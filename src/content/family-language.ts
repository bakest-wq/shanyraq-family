/**
 * Unified family language — single source of truth for all user-facing copy.
 *
 * Tone: warm, simple, respectful, elder-friendly, Kazakh-first (kk).
 * Avoid developer/system wording (JSON, export, sync, API, etc.).
 *
 * Usage:
 *   import { familyText, FAMILY_LANGUAGE } from '@/content/family-text';
 *   familyText(FAMILY_LANGUAGE.empty.relatives);
 */
export type FamilyPhrase = {
  kk: string;
  ru: string;
};

export function kk(phrase: FamilyPhrase): string {
  return phrase.kk;
}

export function ru(phrase: FamilyPhrase): string {
  return phrase.ru;
}

export function bilingual(phrase: FamilyPhrase, separator = ' · '): string {
  return `${phrase.kk}${separator}${phrase.ru}`;
}

export function phraseWithName(phrase: FamilyPhrase, name: string): FamilyPhrase {
  return {
    kk: phrase.kk.replace('{name}', name),
    ru: phrase.ru.replace('{name}', name),
  };
}

export function phraseWithToken(phrase: FamilyPhrase, token: string, value: string): FamilyPhrase {
  return {
    kk: phrase.kk.replace(`{${token}}`, value),
    ru: phrase.ru.replace(`{${token}}`, value),
  };
}

export function brokenLinkMessage(field?: string | null): string {
  switch (field) {
    case 'fatherId':
      return kk(FAMILY_LANGUAGE.health.brokenFatherLink);
    case 'motherId':
      return kk(FAMILY_LANGUAGE.health.brokenMotherLink);
    case 'spouseId':
      return kk(FAMILY_LANGUAGE.health.brokenSpouseLink);
    default:
      return kk(FAMILY_LANGUAGE.health.brokenLinkGeneric);
  }
}

